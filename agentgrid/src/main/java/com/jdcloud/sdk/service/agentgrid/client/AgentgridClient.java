    public interface Builder {
        public Builder credentialsProvider(CredentialsProvider provider);
        public Builder environment(Environment environment);
        public Builder httpRequestConfig(HttpRequestConfig config);
        public AgentgridClient build() throws JdcloudSdkException;
    }

    private static class DefaultBuilder implements Builder {

        private AgentgridClient agentgridClient;

        public DefaultBuilder() {
            agentgridClient = new AgentgridClient();
        }

        public DefaultBuilder credentialsProvider(CredentialsProvider provider) {
            agentgridClient.credentialsProvider = provider;
            return this;
        }

        public DefaultBuilder httpRequestConfig(HttpRequestConfig config) {
            agentgridClient.httpRequestConfig = config;
            return this;
        }

        public AgentgridClient build() throws JdcloudSdkException {

            if (agentgridClient.credentialsProvider == null) {
                //if credentialsProvider not set, try load jdcloud global default  credentials provider
                agentgridClient.credentialsProvider = Jdcloud.defaultInstance().getCredentialsProvider();
                if (agentgridClient.credentialsProvider == null) {
                    throw new JdcloudValidateException("AgentgridClient build error: jdcloud credentials provider not set");
                }
            }
            if (agentgridClient.httpRequestConfig == null) {
                agentgridClient.httpRequestConfig = Jdcloud.defaultInstance().getHttpRequestConfig();
                if (agentgridClient.httpRequestConfig == null) {
                    throw new JdcloudValidateException("AgentgridClient build error: http request config not set");
                }
            }
            return agentgridClient;
        }

        public Builder environment(Environment environment) {
            agentgridClient.environment = environment;
            return this;
        }
    }
